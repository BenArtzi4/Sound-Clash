NIQ = re.compile(r"[֑-ׇ]")
def norm(s):
    s = NIQ.sub("", s or "")
    s = "".join(c for c in unicodedata.normalize("NFKD", s) if not unicodedata.combining(c)).casefold()
    return re.sub(r"[^\w֐-׿]+", " ", s).strip()

D = Path(__file__).parent
batch = json.loads((D / "candidates.js").read_text(encoding="utf-8").split("=", 1)[1].rsplit(";", 1)[0])
import csv
prod = list(csv.DictReader(open(D / "prod_catalog.csv", encoding="utf-8-sig")))
existing_titles = {norm(r["title"]) for r in batch} | {norm(r.get("title", "")) for r in prod}

new = []
seen = set()
for a, t in SONGS:
    nt = norm(t)
    if nt in existing_titles or nt in seen:
        continue
    seen.add(nt)
    new.append((a, t))

print(f"chart songs: {len(SONGS)} | already in batch/catalog: {len(SONGS)-len(new)} | NET-NEW to find ids: {len(new)}")
for a, t in new:
    print(f"  {a} — {t}")
